<?php

$a = 10;
$b = 2;

// SOMA
echo $a + $b;

echo "\n"; // Pulando linha

// SUBTRAÇÃO
echo $a - $b;

echo "\n"; // Pulando linha

// DIVISAO
echo $a / $b;

echo "\n"; // Pulando linha

// MULTIPLICACAO
echo $a * $b;

echo "\n"; // Pulando linha

// RESTO DA DIVISÃO
echo $a % $b;
// Nos numeros pares, o resto será sempre igual a 0;

echo "\n"; // Pulando linha

// POTÊNCIA (X ELEVADO A N)
echo $a ** $b;

echo "\n"; // Pulando linha

// MÓDULO DE UM NUMERO
echo abs($a - 20);


?>