<?php

// Incremento e decremento após a declaração da variável
$acrescimo = 1;
$acrescimo ++; // 1 + 1
print($acrescimo."\n"); // 2

$decrescimo = 1;
$decrescimo --; // 1 - 1
print($decrescimo."\n"); // 0

// Incremento e decremento antes da declaração da variável
$a = 10;
print(++$a."\n"); // 11

$b = 10;
print(--$b."\n"); // 11

?>